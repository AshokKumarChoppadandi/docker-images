#!/bin/sh
#set -e

PROPERTIES_FILE=$CONFLUENT_HOME/config/server.properties
echo "BROKER ID :::: ${BROKER_ID}"
sed -i -e "s/BROKER_ID/$BROKER_ID/" $PROPERTIES_FILE
sed -i -e "s/KAFKA_LISTENERS/$KAFKA_LISTENERS/" $PROPERTIES_FILE
sed -i -e "s/KAFKA_ADVERTISED_LISTENERS/$KAFKA_ADVERTISED_LISTENERS/" $PROPERTIES_FILE
sed -i -e "s/KAFKA_DATA_DIRS/$KAFKA_DATA_DIRS/" $PROPERTIES_FILE
sed -i -e "s/DEFAULT_KAFKA_TOPIC_PARTITIONS/$DEFAULT_KAFKA_TOPIC_PARTITIONS/" $PROPERTIES_FILE
sed -i -e "s/ZOOKEEPERS_LIST/$ZOOKEEPERS_LIST/" $PROPERTIES_FILE
sed -i -e "s/CONFLUENT_METADATA_SERVER_ADVERTISED_LISTENERS/$CONFLUENT_METADATA_SERVER_ADVERTISED_LISTENERS/" $PROPERTIES_FILE

echo "Starting Kafka Broker on $KAFKA_ADVERTISED_LISTENERS"
kafka-server-start $PROPERTIES_FILE
